package com.app;

/**
 * อินเตอร์เฟซ {@code Manageable} ใช้สำหรับกำหนดสัญญา (contract)
 * ให้คลาสที่ต้องสามารถลบข้อมูลด้วยรหัส (ID) ได้
 *
 * <p>เหมาะสำหรับใช้ในระบบที่ต้องการให้แต่ละคลาสมีความสามารถในการลบข้อมูลของตัวเอง</p>
 *
 * @author คุณ
 */
public interface Manageable {

    /**
     * ลบข้อมูลตามรหัสที่กำหนด
     *
     * @param id รหัสของข้อมูลที่ต้องการลบ
     * @return {@code true} ถ้าลบสำเร็จ, {@code false} ถ้าลบไม่สำเร็จหรือไม่พบข้อมูล
     */
    boolean deleteById(int id);
}
